require('./bootstrap');

window.Vue = require('vue');

Vue.mixin({
  methods: {
    numFormat: number => {
	    var parts = number.toString().split(".");
	    parts[0] = parts[0].replace(/\B(?=(\d{3})+(?!\d))/g, ",");
	    return parts.join(".");
	}
  }
})
